/**
 * Get all disclaimers for a specific context
 */
export function getDisclaimersForContext(context: 'onboarding' | 'evidence_pack' | 'sales' | 'privacy'): string[] {
    const all = Object.values(TRUST_DISCLAIMERS);

    switch (context) {
        case 'onboarding':
            return all.map(d => d.short);

        case 'evidence_pack':
            return [
                CONTEXT_DISCLAIMERS.evidence_pack,
                TRUST_DISCLAIMERS.no_grading.long,
                TRUST_DISCLAIMERS.no_inspection_prediction.long,
            ];

        case 'sales':
            return all.map(d => `${d.icon} ${d.short}`);

        case 'privacy':
            return all.map(d => d.long);

        default:
            return all.map(d => d.short);
    }
}

/**
 * Disclaimer component props (for UI rendering)
 */
export interface DisclaimerProps {
    disclaimerId: TrustDisclaimerId;
    variant: 'short' | 'long' | 'with-icon';
}

/**
 * Get formatted disclaimer text
 */
export function getDisclaimerText(id: TrustDisclaimerId, variant: 'short' | 'long' | 'with-icon' = 'short'): string {
    const disclaimer = TRUST_DISCLAIMERS[id];

    switch (variant) {
        case 'long':
            return disclaimer.long;
        case 'with-icon':
            return `${disclaimer.icon} ${disclaimer.short}`;
        case 'short':
        default:
            return disclaimer.short;
    }
}

